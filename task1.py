# Takeing input Two Number From The User
a = int(input("Enter first number: "))
b = int(input("Enter second number: "))

# Adding Two Number
c = a + b
print("Addition =", c)

# Subtracting Two Number
c = a - b
print("Subtraction =", c)

# Multiplying Two Number
c = a * b
print("Multiplication =", c)

# Dividing Two Number
c = a / b
print("Division =", c)
